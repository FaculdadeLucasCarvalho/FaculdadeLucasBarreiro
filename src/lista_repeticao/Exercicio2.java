package lista_repeticao;

public class Exercicio2 {

    public static void main(String[] args) {


        int i;

        System.out.println("Forma crescente:");
        for (i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println("Forma decrescente:");
        for (i = 10; i >= 1; i--) {
            System.out.print(i + " ");
        }
    }
}
